def next_exam_date(goal, today=None):
    """목표 자격증의 다음(오늘 이후) 시험일을 반환. 없으면 None."""
    today = today or date.today()
    raw = CERT_EXAM_DATES_2026.get(goal)
    if not raw:
        # 부분 일치 (예: '정보처리기사 필기'가 정확히 없을 때 대비)
        for k, v in CERT_EXAM_DATES_2026.items():
            if k in goal or goal in k:
                raw = v
                break
    if not raw:
        return None
    upcoming = [datetime.strptime(d, "%Y-%m-%d").date() for d in raw
                if datetime.strptime(d, "%Y-%m-%d").date() >= today]
    return min(upcoming) if upcoming else None


def _norm(s):
    return (s or "").replace(" ", "")


def get_cert_guide(major):
    """전공명으로 자격증 가이드 dict 반환 (부분 일치). 없으면 None."""
    um = _norm(major)
    if not um:
        return None
    for key, guide in CERT_GUIDE.items():
        if key in um or _norm(guide["label"]) in um or um in _norm(guide["label"]):
            return guide
    return None


def cert_guide_text(major):
    """AI 프롬프트에 주입할 자격증 가이드 텍스트. 매칭 안 되면 빈 문자열."""
    g = get_cert_guide(major)
    if not g:
        return ""
    lines = [f"[{g['label']} 자격증 가이드: 아래 실제 정보 기반으로 추천할 것]"]
    lines.append(f"- 필수: {g['required']}")
    lines.append("- 추천 자격증: " + "; ".join(g["recommended"]))
    lines.append("- 진로별 추천 조합:")
    for career, combo in g["byCareer"].items():
        lines.append(f"  · {career}: {combo}")
    if g.get("roadmap"):
        lines.append(f"- 학년별 로드맵: {g['roadmap']}")

    # 2026 시험 일정 (이 전공 관련 자격증만)
    joined = " ".join(g["recommended"]) + " " + " ".join(g["byCareer"].values()) + " " + g.get("required", "")
    sched = [f"  · {name}: {info}" for name, info in CERT_SCHEDULE.items() if name in joined]
    if sched:
        lines.append("- 2026 시험 일정 (학기 배치 시 이 날짜 기준으로 추천할 것):")
        lines.extend(sched)

    lines.append(f"- 참고: {g['note']}")
    return "\n".join(lines)
